package au.edu.unsw.soacourse.dto;

public class EntityMapping {
	int jobId;
	int manager_id;
	String review_team;
	String polls;
	String applications;
	/**
	 * @return the jobId
	 */
	public int getJobId() {
		return jobId;
	}
	/**
	 * @param jobId the jobId to set
	 */
	public void setJobId(int jobId) {
		this.jobId = jobId;
	}
	/**
	 * @return the manager_id
	 */
	public int getManager_id() {
		return manager_id;
	}
	/**
	 * @param manager_id the manager_id to set
	 */
	public void setManager_id(int manager_id) {
		this.manager_id = manager_id;
	}
	/**
	 * @return the review_team
	 */
	public String getReview_team() {
		return review_team;
	}
	/**
	 * @param review_team the review_team to set
	 */
	public void setReview_team(String review_team) {
		this.review_team = review_team;
	}
	/**
	 * @return the polls
	 */
	public String getPolls() {
		return polls;
	}
	/**
	 * @param polls the polls to set
	 */
	public void setPolls(String polls) {
		this.polls = polls;
	}
	/**
	 * @return the applications
	 */
	public String getApplications() {
		return applications;
	}
	/**
	 * @param applications the applications to set
	 */
	public void setApplications(String applications) {
		this.applications = applications;
	}
	
	

}
